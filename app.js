let NUSHU_MAP = {};

async function loadNushuData() {
  try {
    const res = await fetch('数据/nushu.json');
    if (!res.ok) throw new Error('Network error');
    const data = await res.json();
    NUSHU_DATA = data.map(item => ({
      hanzi: item.character,
      nushu: item.nushu
    }));
    NUSHU_DATA.forEach(item => {
      if (item.hanzi && item.nushu && item.nushu.length) {
        NUSHU_MAP[item.hanzi] = item.nushu;
      }
    });
  } catch (e) {
    console.error('Failed to load nushu.json:', e);
  }
}

// ===== Get all unique nushu characters =====
function getAllNushuChars() {
  const seen = new Map();
  NUSHU_DATA.forEach(item => {
    if (!seen.has(item.hanzi)) {
      seen.set(item.hanzi, item.nushu);
    }
  });
  return seen;
}

// ===== Convert text to nushu =====
function textToNushu(text) {
  const results = [];
  for (const char of text) {
    const entry = NUSHU_DATA.find(d => d.hanzi === char);
    if (entry) {
      results.push({ char, nushu: entry.nushu, found: true });
    } else {
      results.push({ char, nushu: [char], found: false });
    }
  }
  return results;
}

// ===== Render nushu output HTML =====
function renderNushuOutput(results) {
  return results.map(r => {
    return `<span class="char-item nushu-global" title="${r.nushu.join(' / ')}">${r.nushu[0]}</span>`;
  }).join('');
}

