let currentLang = localStorage.getItem('nushu_lang') || 'zh';

function setLanguage(lang) {
  currentLang = lang;
  localStorage.setItem('nushu_lang', lang);

  // Update all i18n elements
  document.querySelectorAll('[data-i18n]').forEach(el => {
    const key = el.getAttribute('data-i18n');
    if (I18N[lang] && I18N[lang][key]) {
      el.textContent = I18N[lang][key];
    }
  });

  // Update i18n-placeholder elements
  document.querySelectorAll('[data-i18n-placeholder]').forEach(el => {
    const key = el.getAttribute('data-i18n-placeholder');
    if (I18N[lang] && I18N[lang][key]) {
      el.placeholder = I18N[lang][key];
    }
  });

  // Update i18n-alt elements
  document.querySelectorAll('[data-i18n-alt]').forEach(el => {
    const key = el.getAttribute('data-i18n-alt');
    if (I18N[lang] && I18N[lang][key]) {
      el.alt = I18N[lang][key];
    }
  });

  // Update i18n-aria elements (aria-label)
  document.querySelectorAll('[data-i18n-aria]').forEach(el => {
    const key = el.getAttribute('data-i18n-aria');
    if (I18N[lang] && I18N[lang][key]) {
      el.setAttribute('aria-label', I18N[lang][key]);
    }
  });

  // Update page title
  const pageTitleKey = document.querySelector('title[data-i18n]')?.getAttribute('data-i18n');
  if (pageTitleKey && I18N[lang]?.[pageTitleKey]) {
    document.title = I18N[lang][pageTitleKey];
  }

  // Update meta description
  document.querySelectorAll('meta[data-i18n-content]').forEach(el => {
    const key = el.getAttribute('data-i18n-content');
    if (I18N[lang] && I18N[lang][key]) {
      el.content = I18N[lang][key];
    }
  });

  // Update active lang button
  document.querySelectorAll('.lang-btn').forEach(btn => {
    btn.classList.toggle('active', btn.dataset.lang === lang);
  });

  // Re-render page-specific content
  if (typeof onLangChange === 'function') onLangChange(lang);
}

function initLangSwitch() {
  // Inject switcher into all headers
  document.querySelectorAll('.header-inner').forEach(header => {
    // Remove existing if any
    const existing = header.querySelector('.header-lang');
    if (existing) return;

    const langArea = document.createElement('div');
    langArea.className = 'header-lang';
    langArea.innerHTML = `
      <div class="lang-switcher">
        <button class="lang-btn ${currentLang === 'zh' ? 'active' : ''}" data-lang="zh">中文</button>
        <button class="lang-btn ${currentLang === 'en' ? 'active' : ''}" data-lang="en">EN</button>
        <button class="lang-btn ${currentLang === 'nushu' ? 'active' : ''}" data-lang="nushu">𛆁𛈬</button>
      </div>
    `;
    header.appendChild(langArea);

    langArea.querySelectorAll('.lang-btn').forEach(btn => {
      btn.addEventListener('click', () => setLanguage(btn.dataset.lang));
    });
  });

  // Apply saved language
  setLanguage(currentLang);
}

function getRandomNushu() {
  const entry = NUSHU_DATA[Math.floor(Math.random() * NUSHU_DATA.length)];
  const nushuChar = entry.nushu[Math.floor(Math.random() * entry.nushu.length)];
  return nushuChar;
}

function spawnFloatingCard() {
  const container = document.getElementById('floatingCards');
  if (!container) return;

  const existing = container.querySelectorAll('.floating-card:not(.fc-exit)');
  if (existing.length >= 3) return;

  const count = Math.floor(Math.random() * 3) + 1;
  const corners = [
    { x: 8,  y: 10 }, { x: 50, y: 10 }, { x: 85, y: 10 },
    { x: 8,  y: 40 }, { x: 50, y: 40 }, { x: 85, y: 40 },
    { x: 8,  y: 70 }, { x: 50, y: 70 }, { x: 85, y: 70 },
  ];

  const used = [];
  for (let i = 0; i < count; i++) {
    const avail = corners.filter((_, idx) => !used.includes(idx));
    if (!avail.length) break;
    const pick = avail[Math.floor(Math.random() * avail.length)];
    const cornerIdx = corners.indexOf(pick);
    used.push(cornerIdx);

    const nushuChar = getRandomNushu();
    const card = document.createElement('div');
    const fromLeft = Math.random() < 0.5;
    card.className = 'floating-card ' + (fromLeft ? 'fc-enter-left' : 'fc-enter-right');
    card.textContent = nushuChar;
    card.style.left = pick.x + '%';
    card.style.top = pick.y + '%';
    card.style.transform = 'translateY(-50%)';
    container.appendChild(card);
    card.addEventListener('animationend', () => card.remove());
  }
}

function startFloatingCards() {
  if (window.innerWidth < 768) return;
  spawnFloatingCard();
  setInterval(spawnFloatingCard, 2200);
}

